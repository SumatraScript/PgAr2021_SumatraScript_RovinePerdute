public class Citta {

    private int id;
    private String nome;
    private int x;
    private int y;
    private int h;

    public Citta(int id, String nome, int x, int y, int h) {
        this.id = id;
        this.nome = nome;
        this.x = x;
        this.y = y;
        this.h = h;
    }
}
